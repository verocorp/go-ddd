// Command gen emits the two fan-out arms of the decision-3 (no-outward-
// representation) tree as committed generated packages, so CI re-runs the metric
// on inspectable code — see ../../SCORING.md (D3b spine).
//
//   - decoupled/consumerNN     — hold the pure domain (nav.Maneuver) and use a
//     domain accessor; forced-edits = 0 under the outward-format migration.
//   - coupled/fanout/consumerNN — reach through an EMITTING domain
//     (emit.Maneuver.Record().BurnSeconds) to a telemetry-specific field;
//     forced-edits = N, because the migration removes BurnSeconds.
//
// This generated fan-out is the N-scaling arm the contrast measures. Any
// hand-authored diverse coupled patterns / red-team attempts live under coupled/
// and redteam/ (the DIVERSITY + adversary evidence), authored separately. Run via
// `go generate ./rationale/changeability/nooutward` (cwd = the nooutward dir).
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
)

const decoupledTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d holds the PURE domain object (nav.Maneuver) and reads it
// through a domain accessor — never the outward representation. The outward-format
// migration forces 0 edits on it. One of N identical dependents; the decoupled
// arm's forced-edit count stays 0 for any N.
package consumer%02d

import "github.com/verocorp/go-ddd/rationale/changeability/nooutward/nav"

// Use reads the domain object and nothing outward.
func Use(m nav.Maneuver) int64 {
	return m.BurnMillis()
}
`

const coupledTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d reaches through an EMITTING domain (emit.Maneuver) to a
// telemetry-specific field of its outward representation. The outward-format
// migration removes that field, so this dependent is forced to change. One of N
// identical dependents; the coupled arm's forced-edit count is N. Fails to build
// under -tags repv2 because its own source names Record().BurnSeconds.
package consumer%02d

import "github.com/verocorp/go-ddd/rationale/changeability/nooutward/emit"

// Use reaches through the emitted outward representation to a wire-format field.
func Use(m emit.Maneuver) int64 {
	return m.Record().BurnSeconds
}
`

func emit(dir, tmpl string, n int) {
	for i := 0; i < n; i++ {
		pkgDir := filepath.Join(dir, fmt.Sprintf("consumer%02d", i))
		if err := os.MkdirAll(pkgDir, 0o755); err != nil {
			panic(err)
		}
		content := fmt.Sprintf(tmpl, i, i)
		if err := os.WriteFile(filepath.Join(pkgDir, "consumer.go"), []byte(content), 0o644); err != nil {
			panic(err)
		}
	}
}

func main() {
	n := flag.Int("n", 16, "number of consumer packages to emit per arm")
	flag.Parse()

	emit("decoupled", decoupledTmpl, *n)
	emit(filepath.Join("coupled", "fanout"), coupledTmpl, *n)
	fmt.Printf("gen: wrote %d decoupled + %d coupled-fanout consumer packages\n", *n, *n)
}
